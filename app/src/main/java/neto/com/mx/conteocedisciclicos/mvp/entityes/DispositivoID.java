package neto.com.mx.conteocedisciclicos.mvp.entityes;

public class DispositivoID {
    private String versionActual;
    private String imeii;
    private String aplicacionId;

    public String getVersionActual() {
        return versionActual;
    }

    public void setVersionActual(String versionActual) {
        this.versionActual = versionActual;
    }

    public String getImeii() {
        return imeii;
    }

    public void setImeii(String imeii) {
        this.imeii = imeii;
    }

    public String getAplicacionId() {
        return aplicacionId;
    }

    public void setAplicacionId(String aplicacionId) {
        this.aplicacionId = aplicacionId;
    }
}
